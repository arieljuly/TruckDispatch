<script src="https://cdn.jsdelivr.net/npm/sweetalert2@11"></script>
<script>
    if (typeof window.confirmDelete === 'undefined') {
        window.confirmDelete = function (areaId) {
            Swal.fire({
                title: 'Are you sure?',
                text: "You won't be able to revert this!",
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#dc2626',
                cancelButtonColor: '#6b7280',
                confirmButtonText: 'Yes, delete it!',
                cancelButtonText: 'Cancel',
                background: '#ffffff',
            }).then((result) => {
                if (result.isConfirmed) {
                    Swal.fire({
                        title: 'Deleting...',
                        text: 'Please wait',
                        icon: 'info',
                        showConfirmButton: false,
                        allowOutsideClick: false,
                        didOpen: () => {
                            Swal.showLoading();
                            @this.call('deleteArea', areaId);
                        }
                    });
                }
            });
        }
    }

    // Listen for area deleted event to show success message
    document.addEventListener('livewire:initialized', () => {
        Livewire.on('areaDeleted', () => {
            Swal.fire({
                title: 'Deleted!',
                text: 'Area has been deleted successfully.',
                icon: 'success',
                confirmButtonColor: '#4f46e5',
                confirmButtonText: 'OK'
            });
        });
    });
</script>